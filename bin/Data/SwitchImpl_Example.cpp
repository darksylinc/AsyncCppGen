
struct AsyncSwitchBaseClass
{
	uint32_t internalIdx;
	AsyncSwitchBaseClass( uint32_t _internalIdx ) : internalIdx( _internalIdx ) {}
	virtual ~AsyncSwitchBaseClass();
};

struct MyCustomEvent : public AsyncSwitchBaseClass
{
	uint32_t arg0;
	uint32_t arg1;
	std::string arg2;

	MyCustomEvent( uint32_t _arg0, uint32_t _arg1, const std::string &arg2 ) :
		AsyncSwitchBaseClass( AUTOGENERATED_IDX ),
		arg0( _arg0 ),
		arg1( _arg1 ),
		arg2( _arg2 )
	{
	}
};

void MasterClassName::processAsyncEvent( AsyncSwitchBaseClass *base )
{
	switch( base->internalIdx )
	{
	case AUTOGENERATED_IDX:
	{
		MyCustomEvent *derived = static_cast<MyCustomEvent *>( base );
		derived->functionName( derived->arg0, derived->arg1, derived->arg2 );
		break;
	}
	}
}
